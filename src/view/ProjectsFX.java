package view;

import model.Project;

public class ProjectsFX {

    int id;
    String name;

    public ProjectsFX(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    @Override
    public String toString() {
        return name;
    }
}
